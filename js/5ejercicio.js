//5.- Escribe un programa de tres líneas que pida un número, pida otro 
// número y escriba el resultado de sumar estos dos números.
//Nota: Tener en cuenta la siguiente función: parseInt

let n1 = parseInt(prompt("Ingrese el primer número:"));
let n2 = parseInt(prompt("Ingrese el segundo número:"));
document.write(`La suma de los dos numeros ingresados es  : ${n1 + n2} `);